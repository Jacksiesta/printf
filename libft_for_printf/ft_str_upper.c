#include "libft.h"

char	*ft_str_upper(char *str)
{
	int x;

	x = 0;
	while (str[x])
	{
		if (str[x] >= 'a' && str[x] <= 'z')
			str[x] = str[x] - 32;

	x++;
	}
	return (str);
}
